namespace BaseNode;

/// <summary>
/// Conditional types.
/// </summary>
public enum ConditionalTypes
{
    /// <summary>
    /// Logical 'and'.
    /// </summary>
    And,

    /// <summary>
    /// Logical 'or'.
    /// </summary>
    Or,

    /// <summary>
    /// Logical 'exclusive or'.
    /// </summary>
    Xor,

    /// <summary>
    /// Implication.
    /// </summary>
    Implies,
}
